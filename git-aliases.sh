# to checkout main branch
git config --global alias.main 'checkout main'
# to checkout new branchs
git config --global alias.go checkout
# to access branch commands
git config --global alias.br 'branch'
# to list all branchs
git config --global alias.lbr 'branch -a'
# to create new branchs and checkout it
git config --global alias.nbr 'branch -m'
# to create new commit
git config --global alias.cm 'commit'
# to create new commit message
git config --global alias.cmm 'commit -m'
# to ammend last commit
git config --global alias.cma 'commit --ammend'
# to check status
git config --global alias.st status
# to diff a file
git config --global alias.df diff
# to push changes
git config --global alias.ps push
# to push force changes
git config --global alias.psf 'push --force'
# to pull to origin changes
git config --global alias.pso 'push -u origin'
# to pull changes
git config --global alias.pl pull
